bool http_conn::read_once()
{
    int bytes_read = 0;
    //LT读取数据：用户没读完，epoll会继续通知内核缓冲区有数据
    if (0 == m_TRIGMode)
    {
        bytes_read = recv(m_sockfd,m_read_buf+m_read_idx,READ_BUFFER_SIZE-m_read_idx,0);
        m_read_idx += bytes_read;
        printf("%s \n",m_read_buf);
        printf("LT 读到 %d个字节 \n",bytes_read);
        if (bytes_read <= 0)
        {
            return false;
        }

        return true;
    }
    //ET读数据
    else
    {
        while(true)
        {
            bytes_read = recv(m_sockfd, m_read_buf + m_read_idx, READ_BUFFER_SIZE - m_read_idx, 0);
            if (bytes_read == -1)
            {
                if (errno == EAGAIN || errno == EWOULDBLOCK)
                    break;
                return false;
            }
            else if (bytes_read == 0)
            {
                return false;
            }
            m_read_idx += bytes_read;
            /*cout<<m_read_buf<<endl;
            cout<<"ET 读到 字节数:"<<bytes_read<<endl;*/
        }
        return true;
    }
}

bool http_conn::write()
{
    int temp = 0;

    if (bytes_to_send == 0)
    {
        utils.modfd(m_epollfd, m_sockfd, EPOLLIN, m_TRIGMode);
        init();
        return true;
    }

    while (1)
    {
        temp = writev(m_sockfd, m_iv, m_iv_count);

        if (temp < 0)
        {
            if (errno == EAGAIN)
            {
                utils.modfd(m_epollfd, m_sockfd, EPOLLOUT, m_TRIGMode);
                return true;
            }
            unmap();
            return false;
        }

        bytes_have_send += temp;   //  已经发送给用户的字节数
        bytes_to_send -= temp;     // 用户缓冲区中剩余需要发送的字节数
        if (bytes_have_send >= m_iv[0].iov_len)  // 写入连接socket的字节数 >= 用户写缓冲区的字节数
        {  // 就是表示已经将用户写缓冲区的数据都发送给用户了
            m_iv[0].iov_len = 0;    // 所以将用户写缓冲区的长度值为0，因为已经都发送了没用了

            // 准备发网页了
            m_iv[1].iov_base = m_file_address + (bytes_have_send - m_write_idx);
            m_iv[1].iov_len = bytes_to_send;
        }
        else    // 响应行与头如果没发完就继续发
        {
            m_iv[0].iov_base = m_write_buf + bytes_have_send;
            m_iv[0].iov_len = m_iv[0].iov_len - bytes_have_send;
        }

        if (bytes_to_send <= 0)
        {
            unmap();
            utils.modfd(m_epollfd, m_sockfd, EPOLLIN, m_TRIGMode);

            if (m_linger)
            {
                init();
                return true;
            }
            else
            {
                return false;
            }
        }
    }
}

